first = int(input("Insira o primeiro número inteiro: "))
second = int(input("Insira o segundo número inteiro: "))

print(f"A soma de {first} e {second} é igual a {first+second}.")
print(f"A diferença de {first} e {second} é igual a {first-second}.")
print(f"A multiplicação de {first} e {second} é igual a {first*second}.")
print(f"A divisão de {first} e {second} é igual a {first/second}.")
print(f"O resto da divisão de {first} e {second} é igual a {first%second}.")